# Privacy Policy — CapitalFlow

**Last Updated: May 24, 2025**

## 1. Data Collection
We collect the following personal data:
- Full Name, Email, Telegram contact
- Company Name, Tax ID (INN) for founders
- Passport photos and selfies (KYC verification)
- IP addresses, device fingerprints, session data
- Transaction history and investment activity

## 2. Purpose of Processing
- Account creation and verification
- Anti-money laundering (AML) compliance
- Customer support and communication
- Platform security and fraud prevention
- Legal and regulatory obligations

## 3. Data Protection
- Passwords: SHA-256 with salt
- Documents: AES-256-GCM encryption at rest
- Transport: TLS 1.3 for all connections
- Access: Role-based access control (RBAC)
- Storage: Isolated encrypted storage for KYC docs

## 4. Data Retention
- Active accounts: Data retained until account deletion
- KYC documents: 5 years (AML compliance)
- Transaction records: 5 years (legal requirement)
- Deleted accounts: Anonymized after 90 days

## 5. Your Rights (GDPR)
- Right to access your data
- Right to rectification
- Right to erasure (right to be forgotten)
- Right to data portability
- Right to restrict processing
- Right to object to processing

## 6. Third-Party Sharing
We never sell your data. Sharing occurs only with:
- Law enforcement (legal request)
- Auditors (for compliance verification)
- Cloud infrastructure (Turso, encrypted storage)

